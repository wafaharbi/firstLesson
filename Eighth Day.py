# The len method return length of string

a = "Hello, World!"

print("The length of string :",len(a))        # print the length of the string
print("\n")


# The lower method return the string in lower case

a = "Hello, World!"
print("The string in lower case ",a.lower(),"\n")      # print the lower case


# The upper method return the string in upper case

a = "Hello, World!"
print("The string in upper case ",a.upper(),"\n")      # print the upper case


# The upper method return the string in upper case

a = "Hello, World!"
print(a.replace("H", "J"), "\n")                            # replace the h to j


# The split method use to split the string

a = "Hello, World!"
print(a.split(","))                                     # return ['Hello' , 'World!']

